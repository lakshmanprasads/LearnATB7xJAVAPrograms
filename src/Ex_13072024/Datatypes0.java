package Ex_13072024;

public class Datatypes0 {
    public static void main(String[] args) {
        // data_type variable_name = variable_value(literal)

        // Data Type
        // Primitive Data Type
        //  boolean
        // true or false
        boolean is_married = true;
        boolean are_you_a_dog = false;
        System.out.println(is_married);
        System.out.println(are_you_a_dog);


        // Numerical
        // integral - byte, short, int, long

        byte b  = 127;   //-127 to 127
        System.out.println(b);
        short s = 10111;  // -32768 to 32767
        System.out.println(s);
        int age = 130;
        System.out.println(age);
        long phone_number = 9876543210L; // 9876543210l;
        System.out.println(phone_number);
        //  char - char  - A,B
        char grade  = 'A';
        System.out.println(grade);
        // floating
        // float
        float pi = 3.14f; // 3.14F;
        System.out.println(pi);
        // double
        double d = 118.654;
        System.out.println(d);

        // Non primitive Data Types
        // String  name -> "pramod" -, number or char or bunch of char
        String name = "Pramod";
        System.out.println(name);
        String name2 = "p";
        System.out.println(name2);
//        char cc = "c
    }
}
