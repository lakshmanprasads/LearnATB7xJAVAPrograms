package Ex_13072024;

public class Datatypes3
{
    public static void main(String[] args)
    {
        double a = 15;
        double b = 42;
        double sum  = a+b;
        double sub  = a-b;
        double mul  = a*b;
        double div  = a/b;

        System.out.printf("Sum is %f\n",sum);

        System.out.printf("Sub is %f\n",sub);

        System.out.printf("Mul is %f\n",mul);

        System.out.printf("Div is %f\n",div);
   }

}
