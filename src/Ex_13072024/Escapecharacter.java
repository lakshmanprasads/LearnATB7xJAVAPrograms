package Ex_13072024;

public class Escapecharacter
{
    public static void main(String[] args)
    {
        // Escape Chars
        char c = '\n'; // New line / Next Line
        char c1 = '\t'; // Add Tab
        char c2 = '\b'; // Backslash, delete 1 char
        char c3 = '\r'; // Backslash, delete 1 word
        System.out.println("lakshman" + c3 + "prasad");
        System.out.println("lakshman" + c2 + "prasad");
        System.out.println("lakshman" + c1 + "prasad");
        System.out.println("lakshman" + c + "prasad");
    }
}
