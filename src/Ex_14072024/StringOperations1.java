package Ex_14072024;

public class StringOperations1
{
    public static void main(String[] args)
    {
        //String concat
        String name1 = "sdfs";
        String name2 = "qwer";
        System.out.println(name1+name2+88+55);
        System.out.println(name1+name2+(88+55));
        System.out.println(name1+88+name2+55);
        System.out.println(88+55+name1+name2);
    }
}
