package Ex_2172024;

public class condition_loops16
{
    public static void main(String[] args)
    {
        // while loop
        // A,B,C
        int i=0;  // A - initialization
        while(i<10)  // B- condition
        {
            System.out.println(i);
            i++;             // C- increment/decrement
        }
    }
}
